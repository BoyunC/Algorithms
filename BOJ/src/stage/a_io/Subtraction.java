package stage.a_io;

/*
    문제 1001번 : A-B
*/

import java.util.Scanner;

public class Subtraction {
    public static void main(String[] args){
        Scanner kb = new Scanner(System.in);

        int n = kb.nextInt();
        int m = kb.nextInt();

        System.out.println(n+m);

        kb.close();
    }
}
